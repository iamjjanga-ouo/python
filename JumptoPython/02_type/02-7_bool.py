'''
불(bool) 자료형

- True와 False처럼 항상 대문자로 사용
'''

# 자료형의 참과 거짓
'''
"Python"    참
""          거짓
[1,2,3]     참
[]          거짓
()          거짓
{}          거짓
1           참
0           거짓
None        거짓
'''

# 불 연산
bool('python')      # True
bool('')            # False
bool([1,2,3])       # True
bool([])            # False
bool(0)             # False
bool(3)             # True